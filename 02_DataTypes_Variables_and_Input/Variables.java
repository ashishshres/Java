public class Variables {
  public static void main(String[] args) {
    int myNumber = 10;
    System.out.println(myNumber); // 10

    int yourNumber;
    yourNumber = 20;
    System.out.println(yourNumber); // 20

    float myFloat = 5;
    System.out.println(myFloat); // 5.0

    // float newFloat = 5.0; // cannot convert from double to float
    float newFloat = 5.0f;
    System.out.println(newFloat); // 5.0

    double myDouble = 1.23456;
    System.out.println(myDouble); // 1.23456

    boolean isLoggedIn = false;
    System.out.println(isLoggedIn); // false

    char myCharacter = 'S';
    System.out.println(myCharacter); // S

    String wishes = "Good Morning!";
    System.out.println(wishes); // Good Morning!
  }
}
