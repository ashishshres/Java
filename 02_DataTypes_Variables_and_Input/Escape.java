public class Escape {
  public static void main(String[] args) {
    System.out.println("Hello, \'Good Morning!\'"); // Hello, 'Good Morning'
    System.out.println("Hello,\nGood Morning!");
    /*
     * Hello,
     * Good Morning!
     */
    System.out.println("Hello,\tGood Morning"); // Hello, Good Morning
  }
}
